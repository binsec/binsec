open Loader_buf

let read_addr cursor =
  match Kernel_options.Machine.bits () with
  | `x32 -> Read.u32 cursor
  | `x64 -> Read.u64 cursor
  | _ -> Dwarf_options.Logger.fatal "unexpected architecture bit size"
